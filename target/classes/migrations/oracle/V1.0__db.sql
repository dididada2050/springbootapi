   (    "ID" VARCHAR2(36) NOT NULL ENABLE,
    "CODEID" VARCHAR2(3) NOT NULL ENABLE,
    "CODE" CHAR(2) NOT NULL ENABLE,
    "NAME" VARCHAR2(10) NOT NULL ENABLE,
    "OPTION1" VARCHAR2(10),
    "ORDERCOL" NUMBER(*,0),
     CONSTRAINT "BCODENAME_PK" PRIMARY KEY ("ID"));

